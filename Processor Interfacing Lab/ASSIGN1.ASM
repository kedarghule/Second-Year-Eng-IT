.model Small
.data
MSG1 DB 10,13,"Addition is : $"
ARRAY DB 01H,02H,03H,04H,05H
CNT1 DB 05H
CNT2 DB 05H
CAR DB 00H
.code
MOV AX,@DATA
MOV DS,AX

DISP MACRO MESS
MOV AH,09H
LEA DX,MESS
INT 21H
ENDM

MOV BL,00H
LEA SI,ARRAY
UP2: ADD BL,[SI]
JNC C1
INC CAR
C1:INC SI
DEC CNT2
JNZ UP2

DISP MSG1
MOV BH,CAR
;2 Digit Display Routine 
MOV CH,04H
MOV CL,04H
UP3: ROL BX,CL
MOV DX,BX
AND DX,0FH
CMP DX,09H
JBE DOWN
ADD DX,07H
DOWN:ADD DX,30H
MOV AH,02H
INT 21H
DEC CH
JNZ UP3

MOV AH,4CH
INT 21H
end

